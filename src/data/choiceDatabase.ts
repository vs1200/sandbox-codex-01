export const CHOICE_DB: ChoiceEntry[] = RAW_CHOICE_DB.map(
  ([mino, currentTane, nextTane]) => ({
    mino: mino as MinoType,
    currentTane,
    nextTane,
  }),
);

/**
 * 高速検索用のマップ: mino + tane -> nextTane[]
 */
const choiceMap = new Map<string, number[]>();

for (const entry of CHOICE_DB) {
  const key = `${entry.mino}_${entry.currentTane}`;
  const existing = choiceMap.get(key);
  if (existing) {
    existing.push(entry.nextTane);
  } else {
    choiceMap.set(key, [entry.nextTane]);
  }
}

export function searchChoices(mino: MinoType, tane: number): number[] {
  return choiceMap.get(`${mino}_${tane}`) ?? [];
}
